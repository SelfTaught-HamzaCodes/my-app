/** Picks one prompt at random for the given theme (used when the user selects a theme on Reflect). */
export function getRandomPrompt(theme) {
  const list = prompts[theme];
  if (!list || list.length === 0) return '';
  return list[Math.floor(Math.random() * list.length)];
}
